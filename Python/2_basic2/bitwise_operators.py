"""
Bitwise operators are '&' '|' '~' '^' that allows us to manipulate single bits of data and return 0 or 1 based on the value of the
bits that are used
Bit shifting can be done with << and >> operators

& is used for bitwise conjunction
| is used for bitwise disjunction
^ is used as an or operator
~ is used for negation. It returns 1 for 0 and 0 for 1

Bit shifting:
"""

print(15 & 22)
print(15 | 22)
print(15 ^ 22)
print(22 >> 1) # Right shift
print(22 << 1) # Left shift


